// 경력 항목 하나 만드는 짧은 헬퍼. link·platformCustom처럼 대부분 비워두는 필드는 기본값으로 채운다.
function career(
  id: string,
  partial: Omit<Partial<CareerEntry>, "id" | "platformCustom" | "memo" | "link"> &
    Pick<CareerEntry, "title" | "platform" | "startYear" | "startMonth" | "parts"> & {
      endYear?: number | null;
      endMonth?: number | null;
      memo?: string;
      link?: string;
    }
): CareerEntry {
  return {
    id,
    platformCustom: "",
    endYear: null,
    endMonth: null,
    memo: "",
    link: "",
    ...partial,
  };
}

